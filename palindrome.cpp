//	 write c program to print number is palindrome or not?
#include<stdio.h>
int main()
{
	int r,c,x=0,n;
	printf("enter a number");
	scanf("%d",&n);
	c=n;
	while(n>0)
	{
		r=n%10;
		x=x*10+r;
		n=n/10;
	}
	if(c==x)
	{
		printf("the number is palindrome");
	}
	else
	{
		printf("the number is not palindrome");
		
}
}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

